// ─── Deterministic value generators (per column type, seeded) ─────────────────────────────────

const WORDS = ['alpha', 'bravo', 'charlie', 'delta', 'echo', 'foxtrot', 'golf', 'hotel', 'india', 'juliet'];
const FIRST = ['Ada', 'Alan', 'Grace', 'Linus', 'Margaret', 'Dennis', 'Barbara', 'Ken', 'Edsger', 'Donald'];
const LAST = ['Lovelace', 'Turing', 'Hopper', 'Torvalds', 'Hamilton', 'Ritchie', 'Liskov', 'Thompson', 'Dijkstra', 'Knuth'];

function pick<T>(rng: () => number, arr: T[]): T {
	return arr[Math.floor(rng() * arr.length)];
}

function uuidLike(rng: () => number): string {
	const hex = () => Math.floor(rng() * 16).toString(16);
	const block = (n: number) => Array.from({ length: n }, hex).join('');
	return `${block(8)}-${block(4)}-4${block(3)}-${block(4)}-${block(12)}`;
}

function isoDate(rng: () => number): string {
	const year = 2020 + Math.floor(rng() * 6);
	const month = String(1 + Math.floor(rng() * 12)).padStart(2, '0');
	const day = String(1 + Math.floor(rng() * 28)).padStart(2, '0');
	return `${year}-${month}-${day}`;
}

function isoDateTime(rng: () => number): string {
	const hh = String(Math.floor(rng() * 24)).padStart(2, '0');
	const mm = String(Math.floor(rng() * 60)).padStart(2, '0');
	return `${isoDate(rng)}T${hh}:${mm}:00Z`;
}

const COLORS = ['#3b82f6', '#ef4444', '#22c55e', '#f59e0b', '#a855f7', '#06b6d4'];

// Self-contained data-URI SVG so image/file/video/audio/upload cells render a real
// thumbnail in stories (no backend / no network) instead of a broken-image glyph. The
// fill + label vary by type and row so the media columns are visually distinct + testable.
function placeholderMedia(bg: string, label: string): string {
	const svg = `<svg xmlns='http://www.w3.org/2000/svg' width='96' height='96'><rect width='96' height='96' fill='${bg}'/><text x='48' y='54' font-family='system-ui,sans-serif' font-size='13' font-weight='700' fill='white' text-anchor='middle'>${label}</text></svg>`;
	return `data:image/svg+xml,${encodeURIComponent(svg)}`;
}

// Generate one cell value appropriate to the (forced) CellType, keyed off the column name +
// metadata. Mirrors display-cases.ts representative values so each view paints a realistic cell.
function valueForField(spec: FieldSpec, rng: () => number, rowIndex: number): unknown {
	const alias = spec.pgAlias;
	const name = spec.name;

	if (name === 'id') return uuidLike(rng);

	if (spec.isArray) {
		switch (alias) {
			case 'uuid-array':
				return [uuidLike(rng), uuidLike(rng)];
			case 'number-array':
				return [Number((rng() * 100).toFixed(2)), Number((rng() * 100).toFixed(2))];
			case 'integer-array':
				return [Math.floor(rng() * 100), Math.floor(rng() * 100)];
			case 'date-array':
				return [isoDate(rng), isoDate(rng)];
			default:
				return [pick(rng, WORDS), pick(rng, WORDS), pick(rng, WORDS)];
		}
	}

	switch (alias) {
		case 'text':
			return `${pick(rng, WORDS)} ${pick(rng, WORDS)}`;
		case 'textarea':
			return `${pick(rng, WORDS)}\n${pick(rng, WORDS)} ${pick(rng, WORDS)}`;
		case 'email':
			return `${pick(rng, FIRST).toLowerCase()}.${pick(rng, LAST).toLowerCase()}@example.com`;
		case 'url':
			return `https://example.com/${pick(rng, WORDS)}/${rowIndex}.html`;
		// `pick(rng, COLORS)` consumes one rng() — same as the old url's `pick(rng, WORDS)` —
		// so non-media columns keep identical seeded values (golden stays minimal).
		case 'image':
			return placeholderMedia(pick(rng, COLORS), 'IMG');
		case 'video':
			return placeholderMedia(pick(rng, COLORS), 'VIDEO');
		case 'audio':
			return placeholderMedia(pick(rng, COLORS), 'AUDIO');
		case 'phone':
			return `+1 555 ${String(1000 + Math.floor(rng() * 8999))}`;
		case 'citext':
			return `${cap(pick(rng, WORDS))}Case`;
		case 'bpchar':
			return `${pick(rng, WORDS).slice(0, 2).toUpperCase()} `;
		case 'number':
			return Number((rng() * 1000).toFixed(2));
		case 'integer':
			return Math.floor(rng() * 1000);
		case 'smallint':
			return Math.floor(rng() * 100);
		case 'decimal':
			return (rng() * 1000).toFixed(2);
		case 'currency':
			return Number((rng() * 5000).toFixed(2));
		case 'percentage':
			return Number(rng().toFixed(2));
		case 'date':
			return isoDate(rng);
		case 'datetime':
		case 'timestamptz':
			return isoDateTime(rng);
		case 'time':
			return `${String(Math.floor(rng() * 24)).padStart(2, '0')}:${String(Math.floor(rng() * 60)).padStart(2, '0')}:00`;
		case 'interval':
			return { years: Math.floor(rng() * 3), months: Math.floor(rng() * 12), days: Math.floor(rng() * 28) };
		case 'boolean':
		case 'toggle':
			return rng() > 0.5;
		case 'bit':
			return rng() > 0.5 ? '1' : '0';
		case 'json':
			return { key: pick(rng, WORDS), n: Math.floor(rng() * 100), ok: rng() > 0.5 };
		case 'jsonb':
			return { nested: { items: [Math.floor(rng() * 10), Math.floor(rng() * 10)] }, tag: pick(rng, WORDS) };
		case 'geometry':
			return { type: 'Point', coordinates: [Number((rng() * 180 - 90).toFixed(4)), Number((rng() * 180 - 90).toFixed(4))] };
		case 'geometry-point':
			return { x: Number((rng() * 100).toFixed(2)), y: Number((rng() * 100).toFixed(2)) };
		case 'geometry-collection':
			return { type: 'GeometryCollection', geometries: [] };
		case 'inet':
			return `${Math.floor(rng() * 256)}.${Math.floor(rng() * 256)}.${Math.floor(rng() * 256)}.${Math.floor(rng() * 256)}`;
		case 'file':
			return { url: placeholderMedia(COLORS[rowIndex % COLORS.length], 'FILE'), name: `${pick(rng, WORDS)}.pdf` };
		case 'upload':
			return { url: placeholderMedia(COLORS[rowIndex % COLORS.length], 'UPLOAD'), path: `/uploads/${pick(rng, WORDS)}-${rowIndex}.bin` };
		case 'uuid':
			return uuidLike(rng);
		case 'color':
			return pick(rng, COLORS);
		case 'rating':
			return 1 + Math.floor(rng() * 5);
		case 'tags':
			return [pick(rng, WORDS), pick(rng, WORDS)].join(', ');
		case 'tsvector':
			return `'${pick(rng, WORDS)}':1A '${pick(rng, WORDS)}':2B`;
		case 'origin':
			return pick(rng, ['system', 'import', 'manual', 'api']);
		case 'relation':
			return { id: uuidLike(rng), displayName: `${pick(rng, FIRST)} ${pick(rng, LAST)}` };
		default:
			return `${pick(rng, WORDS)}-${rowIndex}`;
	}
}

/**
 * Deterministically generate `count` rows for a table built by the builders above.
 * The same (table fields, seed, count) always yields byte-identical rows.
 */
export function makeRows(table: MetaTable, count: number, seed = 1): SheetsRow[] {
	const rng = mulberry32(seed);
	const fields = (table.fields ?? []).filter((f): f is MetaField => f != null);
	// Reconstruct the FieldSpec view the generator needs (name + pgAlias + isArray) from meta.
	const specs: FieldSpec[] = fields.map((f) => ({
		name: f.name,
		gqlType: f.type.gqlType,
		pgAlias: f.type.pgAlias ?? undefined,
		isArray: f.type.isArray,
		subtype: f.type.subtype
	}));

	const rows: SheetsRow[] = [];
	for (let i = 0; i < count; i++) {
		const row: SheetsRow = {};
		for (const spec of specs) {
			row[spec.name] = valueForField(spec, rng, i);
		}
		// Guarantee a unique, stable id even if the table has no explicit id field.
		if (row.id == null) row.id = uuidLike(rng);
		rows.push(row);
	}
	return rows;
}

/** Convenience: an all-cell-types table + a matching small dataset. */
export function buildAllCellTypesFixture(rowCount = 25, seed = 7): { table: MetaTable; rows: SheetsRow[] } {
	const table = buildAllCellTypesTable();
	return { table, rows: makeRows(table, rowCount, seed) };
}
