// Deterministic _meta + row fixtures for the Storybook showcase.
//
// Cell type is resolved from SCHEMA META, never the value (resolveCellType). pgAlias is the
// forcing channel: setting field.type.pgAlias to a target type drives mapToFrontendCellType to
// that CellType regardless of gqlType (see cell-types/type-mapping.ts mapAliasToCellType).
// buildAllCellTypesTable emits ONE field per (forceable) CellType so the grid paints every view.
//
// Row values mirror grid/__golden__/display-cases.ts patterns (strings, numbers, booleans, ISO
// dates, arrays, {id,displayName} relations, image urls, geojson, …) but are generated by a
// seeded PRNG (mulberry32) so makeRows(n) is fully reproducible — required for a stable 10k
// stress test and visual regression.
import type { MetaTable, MetaField } from '../../forms/types';
