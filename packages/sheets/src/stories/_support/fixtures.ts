// ─── Seeded PRNG (mulberry32) — tiny, deterministic, NOT Math.random ─────────────────────────
function mulberry32(seed: number): () => number {
	let a = seed >>> 0;
	return function () {
		a |= 0;
		a = (a + 0x6d2b79f5) | 0;
		let t = Math.imul(a ^ (a >>> 15), 1 | a);
		t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
		return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
	};
}

// ─── Meta builders (mirror testing/mock-sheets-provider.tsx buildMetaField/buildMetaTable) ────

interface FieldSpec {
	name: string;
	gqlType: string;
	/** Forces the resolved CellType (pgAlias has top priority in mapToFrontendCellType). */
	pgAlias?: string;
	pgType?: string;
	isArray?: boolean;
	subtype?: string | null;
	isNotNull?: boolean;
	hasDefault?: boolean;
}

function buildMetaField(spec: FieldSpec): MetaField {
	return {
		name: spec.name,
		isNotNull: spec.isNotNull ?? false,
		hasDefault: spec.hasDefault ?? false,
		type: {
			gqlType: spec.gqlType,
			isArray: spec.isArray ?? false,
			pgType: spec.pgType ?? 'text',
			pgAlias: spec.pgAlias ?? null,
			isNotNull: spec.isNotNull ?? false,
			hasDefault: spec.hasDefault ?? false,
			subtype: spec.subtype ?? null
		}
	} as MetaField;
}

interface TableSpec {
	name: string;
	fields: FieldSpec[];
	belongsTo?: Array<{ fieldName: string; references: string; keyName: string }>;
}

function camelPlural(name: string): string {
	// The mock adapter is keyed on table.name only; inflection names are cosmetic for these
	// fixtures, so a naive pluralization is sufficient.
	return name.endsWith('s') ? name : `${name}s`;
}

function buildMetaTable(spec: TableSpec): MetaTable {
	return {
		name: spec.name,
		schemaName: 'public',
		query: {
			all: camelPlural(spec.name),
			create: `create${cap(spec.name)}`,
			delete: `delete${cap(spec.name)}`,
			one: spec.name,
			update: `update${cap(spec.name)}`
		},
		fields: spec.fields.map(buildMetaField),
		inflection: {
			allRows: camelPlural(spec.name),
			connection: camelPlural(spec.name),
			tableType: spec.name
		},
		indexes: [],
		constraints: { primaryKey: null, unique: [], foreignKey: [] },
		foreignKeyConstraints: [],
		primaryKeyConstraints: [],
		uniqueConstraints: [],
		relations: {
			belongsTo: (spec.belongsTo ?? []).map((rel) => ({
				fieldName: rel.fieldName,
				isUnique: false,
				type: 'belongsTo',
				keys: [{ name: rel.keyName, type: { gqlType: 'UUID', isArray: false, pgType: 'uuid' } }],
				references: { name: rel.references }
			})),
			has: [],
			hasOne: [],
			hasMany: [],
			manyToMany: []
		}
	} as unknown as MetaTable;
}

function cap(s: string): string {
	return s.charAt(0).toUpperCase() + s.slice(1);
}

